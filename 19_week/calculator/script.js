class Calculator {
    static showResultSum() {
        let a = prompt("Первое число?");
        let b = prompt("Второе число?");
        alert(+a + +b);
    }
    static showResultSubtraction() {
        let a = prompt("Первое число?");
        let b = prompt("Второе число?");
        alert(a - b);
    }
    static showResultDivision() {
        let a = prompt("Первое число?");
        let b = prompt("Второе число?");
        alert(a / b);
    }
    static showResultMultiplication() {
        let a = prompt("Первое число?");
        let b = prompt("Второе число?");
        alert(a * b);
    }
}
